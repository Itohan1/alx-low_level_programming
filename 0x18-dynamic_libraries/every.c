#include <stdio.h>

/**
 * add - function for multiplication
 * @a: first parameter
 * @b: second parameter
 * Return: 0
 */

int mul(int a, int b)
{
	return(a * b);
}

/**
 * sub - function for subtraction
 * @a: first parameter
 * @b: second parameter
 * Return: 0
 */

int sub(int a, int b)
{
	return(a - b);
}

/**
 * division - function for division
 * @a: first parameter
 * @b: second parameter
 * Return: 0
 */

int div(int a, int b)
{
	return(a / b);
}

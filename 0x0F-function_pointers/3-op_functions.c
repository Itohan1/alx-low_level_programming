#include "3-calc.h"

/**
 * op_add - adds 2 variables
 * @a: first parameter
 * @b: second parameter
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - find the difference between 2 variables
 * @a: first parameter
 * @b: second parameter
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of a and b
 * @a: first parameter
 * @b: second parameter
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - result division of a by b
 * @a: first parameter
 * @b: second parameter
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of a by b
 * @a: first parameter
 * @b: second parameter
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

